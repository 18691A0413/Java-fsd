//Implicit Type Casting
package Assisted_parctice1;

public class Implicit_and_Explicit_type_casting {

	public static void main(String[] args) {
		int a=25;
		double b=a;
		System.out.println("The Implicit type casting is :" +b);
		
 //Explicit Type Casting	
        double c=25.5;
        int d=(int)c;
        System.out.println("The Explicit type casting is :" +d);
	}

}